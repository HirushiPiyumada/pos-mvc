    private void saveCustomer() {
        customermodel customer = new customermodel(
                customeridtextfield.getText(),
                customertitletextfield.getText(),
                customernametextfield.getText(),
                customerdobtextfield.getText(),
                Double.parseDouble(customersalarytextfield.getText()),
                customeraddresstextfield.getText(),
                customercitytextfield.getText(),
                customerprovincetextfield.getText(),
                customerpostalcodetextfield.getText());

        try {
            String resp = Customercontroller.saveCustomer(customer);
            JOptionPane.showConfirmDialog(this, resp);
            clear();
            loadAllCustomers();
        } catch (SQLException ex) {
            Logger.getLogger(customerview.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showConfirmDialog(this, ex.getMessage());
        }
    }

