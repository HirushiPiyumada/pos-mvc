    private void loadAllCustomers() {
        try {
            String[] columns = {"Id", "Name", "Address", "Salary", "PostalCode"};
            DefaultTableModel dtm = new DefaultTableModel(columns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }

            };

            customertable.setModel(dtm);

            ArrayList<customermodel> customers = Customercontroller.getAllCustomers();

            for (customermodel customer : customers) {
                Object[] rowData = {customer.getCustomerid(), customer.getCustomertitle() + " " + customer.getCustomername(), customer.getCustomeraddress() + "," + customer.getCustomercity(), customer.getCustomersalary(), customer.getCustmerpostalcode()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(customerview.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showConfirmDialog(this, ex.getMessage());
        }
    }

    public void searchCustomer() {
        try {
            String custId = customertable.getValueAt(customertable.getSelectedRow(), 0).toString();
            customermodel Customermodel = Customercontroller.getCustomer(custId);

            if (Customermodel != null) {

                customeridtextfield.setText(Customermodel.getCustomerid());
                customertitletextfield.setText(Customermodel.getCustomertitle());
                customernametextfield.setText(Customermodel.getCustomername());
                customeraddresstextfield.setText(Customermodel.getCustomeraddress());
                customerdobtextfield.setText(Customermodel.getCustomerdob());
                customersalarytextfield.setText(Double.toString(Customermodel.getCustomersalary()));
                customercitytextfield.setText(Customermodel.getCustomercity());
                customerprovincetextfield.setText(Customermodel.getCustomerprovince());
                customerpostalcodetextfield.setText(Customermodel.getCustomerpostalcode());
            }
            else{
                 JOptionPane.showConfirmDialog(this, "Customer not Fpund");
            }
        } catch (SQLException ex) {
            Logger.getLogger(customerview.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showConfirmDialog(this, ex.getMessage());
        }
    }
    
